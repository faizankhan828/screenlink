class SeedData {
  static String chatIdFor(String userA, String userB) {
    final ids = [userA, userB]..sort();
    return '${ids[0]}_${ids[1]}';
  }

  static List<ChatThread> chatsForUser(String currentUid) {
    return chats().map((thread) {
      final other = thread.participants.firstWhere((p) => p != 'u1', orElse: () => 'u2');
      return ChatThread(
        chatId: chatIdFor(currentUid, other),
        participants: [currentUid, other],
        title: thread.title,
        avatarUrl: thread.avatarUrl,
        lastMessage: thread.lastMessage,
        updatedAt: thread.updatedAt,
        unreadCount: thread.unreadCount,
      );
    }).toList();
  }

  static List<CreativeMessage> messagesForChat(String chatId, String currentUid) {
    for (final thread in chats()) {
      final other = thread.participants.firstWhere((p) => p != 'u1');
      if (chatIdFor(currentUid, other) != chatId) continue;
      final legacyMessages = messages()[thread.chatId] ?? const <CreativeMessage>[];
      return legacyMessages
          .map(
            (m) => CreativeMessage(
              senderId: m.senderId == 'u1' ? currentUid : m.senderId,
              receiverId: m.receiverId == 'u1' ? currentUid : m.receiverId,
              message: m.message,
              timestamp: m.timestamp,
              read: m.read,
              chatId: chatId,
              imageUrl: m.imageUrl,
            ),
          )
          .toList();
    }
    return const [];
  }

